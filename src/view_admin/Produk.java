    private void jButton8ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton8ActionPerformed
         
        File reportfile = new File(".");
        String dirr ="";
        try{
            Statement statement=Config.Conn.GetConn().createStatement();
            ResultSet res = statement.executeQuery("SELECT tbl_produk.id_produk,tbl_produk.nama_produk,tbl_produk.haarga_produk,tbl_produk.stok,tbl_produk.tanggal FROM tbl_produk  ");
            dirr = reportfile.getCanonicalPath()+"/src/report/";
            JasperDesign design = JRXmlLoader.load(dirr+"laporanproduk.jrxml");
            JasperReport jr = JasperCompileManager.compileReport(design);

            JRResultSetDataSource rsDataSource = new JRResultSetDataSource(res);

            JasperPrint jp = JasperFillManager.fillReport(jr, new HashMap<>(),rsDataSource);
            JasperViewer.viewReport(jp, false);
        }catch(IOException | SQLException | JRException e){
            JOptionPane.showMessageDialog(table_barang1, e);
        }
    }//GEN-LAST:event_jButton8ActionPerformed

